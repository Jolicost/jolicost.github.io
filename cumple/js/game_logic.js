var points = 0;

function refreshPoints() {
    var elems = Array.from(document.getElementsByClassName("checked"));
    var temp_points = 0;
    elems.forEach(function(elem) {
        var add_points = elem.getAttribute("add-points");
        if (add_points) {
            temp_points += parseInt(add_points);
        }
    });  
    points = temp_points;
    document.getElementById("points_tracker").innerHTML = points;
}

function addCheckableLogic() {
    var elems = Array.from(document.getElementsByClassName("checkable"));
    elems.forEach(function(elem) {
        elem.onclick = function(e) {
            this.classList.toggle("checked");
            refreshPoints();
            checkLocked();
        };
    });
}

function checkLocked() {
    var elems = Array.from(document.querySelectorAll('[unlock]'));
    elems.forEach(function(elem) {
        var lockedPoints = parseInt(elem.getAttribute('unlock'));
        if (lockedPoints) { 
            if (lockedPoints >= points) 
            {
                elem.classList.add("locked");
            }
            else 
            {
                elem.classList.remove("locked"); 
            }
        } 
    });
}

function toggleMarcador() {
    var marcador = document.getElementById("marcador_text");
    if (marcador.style.display == 'block') {   
        marcador.style.display='none';
    } else {
        marcador.style.display = 'block';   
    }
}

